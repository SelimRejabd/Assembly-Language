INCLUDE EMU8086.INC
.MODEL SMALL
.STACK 100H
.DATA
    NUM DW 0D
    REMAINDER DB 0D
    QUOTIENT DB 0D
    DIVIDEND DB 2D
.CODE

MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV ES,AX
    PRINT 'INPUT: '
    
    MOV AH,1D
    INT 21H
    SUB AL,48D

    DIV DIVIDEND
    CMP AH,0D
    JE EVEN
    JMP ODD

    EVEN:
    PRINTN ''
    PRINT 'EVEN NUMBER'
    JMP EXIT

    ODD:
    PRINTN ''
    PRINT 'ODD NUMBER'
    EXIT:

    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN